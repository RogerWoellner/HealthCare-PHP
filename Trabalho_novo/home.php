        <h1>Saúde muito mais do que ausência de doenças</h1>
        <p>Podemos partir dessa memória pessoal bastante dolorosa para ilustrar um entendimento peculiar do conceito de saúde: ausência de doença, visão particularmente defendida por um filósofo chamado Christopher Boorse, em 1975.</p> 
        <p>Trata-se de um conceito negativo, ou seja, não explicaria por si só o que seria saúde, mas pondera que ela existiria somente em função da não-doença.</p>
        <p>Na prática, implicaria em considerar saudável uma pessoa com câncer, desde que não apresente sintomas nem tenha recebido ainda diagnóstico. Parece razoável?</p>
