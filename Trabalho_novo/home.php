        <p>É uma visão adotada pela instituição até os dias de hoje – porém também sofre uma série de críticas ao tratar a saúde como uma espécie de utopia, um estado inatingível de completo bem-estar. De acordo com esse conceito, a maioria dos seres humanos não poderia ser considerada saudável, não é mesmo? <strong>Você seria?</strong></p>
