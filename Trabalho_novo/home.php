        <p>Décadas antes, a Organização Mundial de Saúde (OMS) já havia proposto um conceito positivo, que buscou ampliar o entendimento dos fatores que interferem na saúde das pessoas. A saúde seria, de acordo com essa visão, o "completo bem-estar físico, mental e social".</p>
